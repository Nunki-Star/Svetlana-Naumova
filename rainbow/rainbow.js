var rainbowInt = parseInt(prompt("please enter the number of the colour"))
var colourRain;
switch (rainbowInt){
    case 1 :
        colourRain="red";
        break;
    case 2 :
        colourRain="blue";
        break;
    case 3 :
        colourRain="green";
        break;
}
console.log(colourRain)