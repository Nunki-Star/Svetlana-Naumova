function calculator(a, b, operation){
    switch(operation){
        case "+":
            console.log(a+b);
            break
    }
    switch(operation){
        case "-":
            console.log(a-b);
            break
    }
    switch(operation){
        case "*":
            console.log(a*b);
            break
    }
    switch(operation){
        case "/":
            console.log(a/b);
            break
    }
}

calculator(9, 2, "*")

